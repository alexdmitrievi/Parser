def parse_russian_date(raw: str | None) -> Optional[datetime]:
    """Распарсить дату в любом российском/ISO формате → datetime (UTC).

    Поддерживает:
        - ISO 8601 с/без timezone
        - DD.MM.YYYY [HH:MM[:SS]]
        - DD/MM/YYYY, DD-MM-YYYY
        - «15 марта 2026», «15 марта 2026 г., 10:00»
        - Короткий год «15.03.26»
    """
    if not raw:
        return None
    if isinstance(raw, datetime):
        return raw.replace(tzinfo=timezone.utc) if raw.tzinfo is None else raw

    raw = str(raw).strip()

    _ISO_FMTS = [
        "%Y-%m-%dT%H:%M:%S.%f%z",
        "%Y-%m-%dT%H:%M:%S%z",
        "%Y-%m-%dT%H:%M:%S.%f",
        "%Y-%m-%dT%H:%M:%S",
        "%Y-%m-%d %H:%M:%S",
        "%Y-%m-%d %H:%M",
        "%Y-%m-%d",
        "%d.%m.%Y %H:%M:%S",
        "%d.%m.%Y %H:%M",
        "%d.%m.%Y",
        "%d/%m/%Y %H:%M",
        "%d/%m/%Y",
        "%d-%m-%Y %H:%M",
        "%d-%m-%Y",
    ]
    for fmt in _ISO_FMTS:
        try:
            dt = datetime.strptime(raw, fmt)
            return dt if dt.tzinfo else dt.replace(tzinfo=timezone.utc)
        except ValueError:
            pass

    # «15 марта 2026» / «15 марта 2026 г.» / «15 марта 2026, 10:00»
    m = re.search(
        r"(\d{1,2})\s+([а-яёА-ЯЁ]+)\s+(\d{4})(?:\s*г\.?)?"
        r"(?:[,\s]+(\d{1,2}):(\d{2})(?::(\d{2}))?)?",
        raw, re.IGNORECASE,
    )
    if m:
        day, month_str, year = int(m.group(1)), m.group(2).lower(), int(m.group(3))
        hour = int(m.group(4)) if m.group(4) else 0
        minute = int(m.group(5)) if m.group(5) else 0
        second = int(m.group(6)) if m.group(6) else 0
        month = _MONTHS_RU.get(month_str)
        if month:
            try:
                return datetime(year, month, day, hour, minute, second, tzinfo=timezone.utc)
            except ValueError:
                pass

    # Короткий год «15.03.26»
    m2 = re.match(r"^(\d{1,2})\.(\d{1,2})\.(\d{2})$", raw)
    if m2:
        d, mo, y2 = int(m2.group(1)), int(m2.group(2)), int(m2.group(3))
        year = 2000 + y2 if y2 < 70 else 1900 + y2
        try:
            return datetime(year, mo, d, tzinfo=timezone.utc)
        except ValueError:
            pass

    logger.debug(f"parse_russian_date: cannot parse {raw!r}")
    return None


def parse_price(raw: str | float | int | None) -> Optional[float]:
    """Распарсить строку цены → float.

    Поддерживает форматы:
        «1 234 567,89 руб.», «₽ 1 234 567», «1234567.89»,
        «1,234,567.89», «1.234.567,89», «1234567,89»
    """
    if raw is None:
        return None
    if isinstance(raw, (int, float)):
        return float(raw) if raw > 0 else None

    s = str(raw).strip()

    # Удаляем валютные символы и слова
    s = re.sub(
        r"[₽руб\.\s]|RUB|руб\.?|рублей|тыс\.|млн\.?|млрд\.?",
        " ", s, flags=re.IGNORECASE,
    ).strip()

    # Неразрывные пробелы → обычные
    s = s.replace("\u00a0", " ").replace("\u202f", " ")

    # «1 234 567,89» — пробел как тысячный разделитель, запятая как дробная
    if re.search(r"\d\s+\d", s) and "," in s:
        s = re.sub(r"\s+", "", s).replace(",", ".")
    # «1 234 567» — только пробелы (целое)
    elif re.search(r"\d\s+\d", s):
        s = re.sub(r"\s+", "", s)
    # «1.234.567,89» — точка как тысячный, запятая как дробная
    elif re.match(r"^\d{1,3}(\.\d{3})+,\d{1,2}$", s):
        s = s.replace(".", "").replace(",", ".")
    # «1234567,89» — запятая как дробная
    elif re.match(r"^\d+,\d{1,2}$", s):
        s = s.replace(",", ".")
    # «1,234,567.89» — запятая как тысячный, точка как дробная
    elif re.match(r"^\d{1,3}(,\d{3})+(\.\d+)?$", s):
        s = s.replace(",", "")
    else:
        # Убираем оставшиеся запятые и пробелы
        s = re.sub(r"[,\s]", "", s)

    s = re.sub(r"[^\d.\-]", "", s)
    if not s or s in (".", "-", ""):
        return None

    try:
        val = float(s)
        return val if val > 0 else None
    except ValueError:
        return None


