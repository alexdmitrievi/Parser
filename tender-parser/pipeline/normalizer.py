def normalize_region(raw: str | None) -> str | None:
    """Нормализовать название региона по таблице."""
    if not raw:
        return None
    key = raw.strip().lower()
    # Точное совпадение
    if key in _REGION_MAP:
        return _REGION_MAP[key]
    # Подстрочный поиск (короткие аббревиатуры)
    for short, full in _REGION_MAP.items():
        if len(short) >= 4 and short in key:
            return full
    return raw.strip()


def determine_law_type(registry_number: str | None, source: str = "") -> str:
    """Определить тип закона по номеру реестра и источнику."""
    if not registry_number:
        if source in ("eis_ftp", "eis_api", "eis"):
            return "44-fz"
        return "commercial"

    num = registry_number.strip()

    # 44-ФЗ: ровно 19 цифр (0173100000000000000 — классический формат)
    if re.match(r"^\d{19}$", num):
        return "44-fz"
    # 223-ФЗ: ровно 11 цифр
    if re.match(r"^\d{11}$", num):
        return "223-fz"
    # 44-ФЗ по коду ЕИС (начинается с "0" + длинная строка)
    if re.match(r"^0\d{18}$", num):
        return "44-fz"

    if source in ("eis_ftp", "eis_api", "eis"):
        return "44-fz"

    return "commercial"


def normalize_purchase_method(raw: str | None) -> str | None:
    """Нормализовать способ закупки к единому коду."""
    if not raw:
        return None
    lower = raw.lower().strip()
    for key, code in _METHOD_MAP.items():
        if key in lower:
            return code
    return raw


def clean_title(title: str | None) -> str | None:
    """Очистить название тендера: лишние пробелы, HTML-артефакты, длина."""
    if not title:
        return title
    # Убираем HTML-теги, если вдруг попали
    title = re.sub(r"<[^>]+>", " ", title)
    # Нормализуем пробелы
    title = re.sub(r"\s+", " ", title).strip()
    # Не более 500 символов (обрыв по словам)
    if len(title) > 500:
        title = title[:497].rsplit(" ", 1)[0] + "…"
    return title or None


def normalize_tender(tender: TenderCreate) -> TenderCreate:
    """Нормализовать один тендер."""
    tender.title = clean_title(tender.title) or tender.title
    tender.customer_region = normalize_region(tender.customer_region)

    if not tender.law_type or tender.law_type == "commercial":
        tender.law_type = determine_law_type(
            tender.registry_number, tender.source_platform
        )

    tender.purchase_method = normalize_purchase_method(tender.purchase_method)

    # Очищаем длинное описание
    if tender.description and len(tender.description) > 2000:
        tender.description = tender.description[:1997] + "…"

    return tender


def normalize_batch(tenders: list[TenderCreate]) -> list[TenderCreate]:
    """Нормализовать список тендеров."""
    result = [normalize_tender(t) for t in tenders]
    logger.info(f"Normalized {len(result)} tenders")
    return result
